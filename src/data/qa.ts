/**
 * 上下文记忆系统
 * 用于多轮对话的上下文理解
 */
interface ConversationContext {
  history: Array<{ question: string; answer: string; timestamp: number }>
  topics: Map<string, number> // 话题及其出现次数
  entities: Map<string, number> // 实体及其出现次数
}

// 全局对话上下文（可以在实际应用中持久化）
let conversationContext: ConversationContext = {
  history: [],
  topics: new Map(),
  entities: new Map()
}

/**
 * 更新对话上下文
 */
function updateContext(question: string, answer: string, tokens: string[], entities: string[]) {
  // 添加到历史记录
  conversationContext.history.push({
    question,
    answer,
    timestamp: Date.now()
  })

  // 限制历史记录长度
  if (conversationContext.history.length > 10) {
    conversationContext.history.shift()
  }

  // 更新话题统计
  for (const token of tokens) {
    if (token.length > 1) { // 忽略单字
      conversationContext.topics.set(
        token,
        (conversationContext.topics.get(token) || 0) + 1
      )
    }
  }

  // 更新实体统计
  for (const entity of entities) {
    conversationContext.entities.set(
      entity,
      (conversationContext.entities.get(entity) || 0) + 1
    )
  }
}

/**
 * 获取上下文相关的话题
 */
function getContextualTopics(): string[] {
  // 按出现频率排序，返回前 5 个话题
  return Array.from(conversationContext.topics.entries())
    .sort((a, b) => b[1] - a[1])
    .slice(0, 5)
    .map(([topic]) => topic)
}

