/**
 * 高级中文分词算法
 * 使用：最大匹配算法 + 词典优先 + 上下文感知
 */
function advancedTokenize(text: string): string[] {
  const tokens: string[] = []
  let i = 0

  while (i < text.length) {
    let matched = false
    let maxLen = Math.min(10, text.length - i) // 最大匹配长度

    // 从长到短尝试匹配
    for (let len = maxLen; len >= 2; len--) {
      const substring = text.substring(i, i + len)

      // 优先匹配技术词典
      if (techDictionary.has(substring)) {
        tokens.push(substring)
        i += len
        matched = true
        break
      }

      // 匹配领域词典
      if (domainDictionary.has(substring)) {
        tokens.push(substring)
        i += len
        matched = true
        break
      }
    }

    // 如果没有匹配到词典词汇，按单字分词
    if (!matched) {
      const char = text[i]
      // 过滤停用词和标点符号
      if (!stopWords.has(char) && /[\u4e00-\u9fa5a-zA-Z0-9]/.test(char)) {
        tokens.push(char)
      }
      i++
    }
  }

  return tokens
}

/**
 * TF-IDF 算法：计算词汇的重要性
 * TF (Term Frequency): 词频
 * IDF (Inverse Document Frequency): 逆文档频率
 */
function calculateTFIDF(tokens: string[], allDocuments: string[][]): Map<string, number> {
  const tfidf = new Map<string, number>()
  const tokenCount = tokens.length

  // 计算 TF
  const tf = new Map<string, number>()
  for (const token of tokens) {
    tf.set(token, (tf.get(token) || 0) + 1)
  }

  // 计算 IDF 和 TF-IDF
  for (const [token, frequency] of tf.entries()) {
    const termFreq = frequency / tokenCount

    // 计算包含该词的文档数
    const docsWithTerm = allDocuments.filter(doc => doc.includes(token)).length
    const inverseDocFreq = Math.log((allDocuments.length + 1) / (docsWithTerm + 1))

    tfidf.set(token, termFreq * inverseDocFreq)
  }

  return tfidf
}

/**
 * 余弦相似度算法：计算两个向量的相似度
 */
function cosineSimilarity(vec1: Map<string, number>, vec2: Map<string, number>): number {
  let dotProduct = 0
  let norm1 = 0
  let norm2 = 0

  // 计算所有唯一的键
  const allKeys = new Set([...vec1.keys(), ...vec2.keys()])

  for (const key of allKeys) {
    const v1 = vec1.get(key) || 0
    const v2 = vec2.get(key) || 0

    dotProduct += v1 * v2
    norm1 += v1 * v1
    norm2 += v2 * v2
  }

  if (norm1 === 0 || norm2 === 0) return 0

  return dotProduct / (Math.sqrt(norm1) * Math.sqrt(norm2))
}

/**
 * 编辑距离算法（Levenshtein Distance）
 * 用于计算两个字符串的相似度
 */
function levenshteinDistance(str1: string, str2: string): number {
  const m = str1.length
  const n = str2.length
  const dp: number[][] = []

  // 初始化 DP 表
  for (let i = 0; i <= m; i++) {
    dp[i] = []
    for (let j = 0; j <= n; j++) {
      if (i === 0) dp[i][j] = j
      else if (j === 0) dp[i][j] = i
      else dp[i][j] = 0
    }
  }

  // 动态规划计算编辑距离
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      if (str1[i - 1] === str2[j - 1]) {
        dp[i][j] = dp[i - 1][j - 1]
      } else {
        dp[i][j] = 1 + Math.min(
          dp[i - 1][j],     // 删除
          dp[i][j - 1],     // 插入
          dp[i - 1][j - 1]  // 替换
        )
      }
    }
  }

  return dp[m][n]
}

/**
 * 字符串相似度（基于编辑距离）
 */
function stringSimilarity(str1: string, str2: string): number {
  const maxLen = Math.max(str1.length, str2.length)
  if (maxLen === 0) return 1.0

  const distance = levenshteinDistance(str1, str2)
  return 1.0 - distance / maxLen
}

